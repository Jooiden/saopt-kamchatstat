def calculate_results(raw_score, test_id, mu=None, sigma=None):
    """
    Универсальный расчет результатов.
    raw_score: накопленная сумма баллов (число).
    test_id: ID теста из базы данных.
    mu, sigma: параметры из таблицы tests для перевода в станайны.
    """
    try:
        if test_id == 3:
            return round(raw_score / 16, 2)

        if mu is None or sigma is None or sigma == 0:
            return raw_score
        
        # формула перевода в станайны
        stanine = round(2 * ((raw_score - mu) / sigma) + 5.0)
        
        # диапазон от 1 до 9
        return max(1, min(9, stanine))

    except Exception as e:
        logging.error(f"Ошибка в расчетах (test_id {test_id}): {e}")
        return raw_score  # Возвращаем сумму


def get_interpretation(score, test_id):
    """
    Возвращает текстовую расшифровку на основе полученного балла.
    """
    score = int(float(score))
    # 1. Логика для теста №3 (Социально-психологический климат)
    # Используем индекс от -1 до +1
    if test_id == 3:
        if score < -0.33:
            return f"Индекс: {score}. Характеризуется как отрицательный. В коллективе может наблюдаться разобщенность и преобладание пессимистичного настроя."
        elif score > 0.33:
            return f"Индекс: {score}. Характеризуется как положительный. Коллектив сплочен, преобладает бодрый и оптимистичный настрой."
        else:
            return f"Индекс: {score}. Неопределенный (противоречивый) климат. Взаимоотношения в группе нестабильны."

    # 2. Логика для тестов №1 и №2 (Станайны 1-9)
    interpretations = {
        1: { # ОСР в мод. Разуваевой
            "low":  (1, 3, "Показатели в норме. Психоэмоциональное состояние стабильно, адаптационные механизмы сохранены."),
            "mid":  (4, 6, "Средний уровень. Наблюдается умеренное влияние стрессовых факторов, рекомендуется мониторинг состояния."),
            "high": (7, 9, "Показатели выше нормы. Рекомендуется индивидуальная консультация со специалистом для профилактики переутомления.")
        },
        2: { # Басс-Перри
            "low":  (1, 3, "Низкий уровень конфликтности. Характеризуется высоким самообладанием и выдержкой."),
            "mid":  (4, 6, "Средний уровень. Адекватные реакции на внешние раздражители, нормативная активность."),
            "high": (7, 9, "Повышенный уровень экспрессивности. Склонность к активному отстаиванию своей позиции, возможна импульсивность.")
        }
    }
    
    test_data = interpretations.get(test_id)
    if not test_data: 
        return f"Результат: {score}"
        
    for bounds in test_data.values():
        if bounds[0] <= score <= bounds[1]:
            # Возвращаем ТОЛЬКО текст описания
            return bounds[2] 
            
    return "" # Если балл вне диапазона, возвращаем пустоту


